n1 = int(input ('Digite um número'))
n2 = int(input ('Digite um novo número'))
s = n1+n2
p = n1*n2
d = n1/n2
di = n1//n2
e = n1**n2
print ('A soma é {}, o produto é {}, a divisão é {}'.format (s, p, d), end='')
print ('A divisão inteira é {}, \n a potência é {}'.format (di,e))
# o ", end=''" junta as linhas
# o "\n" cria uma nova linha


