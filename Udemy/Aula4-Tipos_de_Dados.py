# Vamos aprender quais os tipos de dados no Python
# Função para identificar tipos de dados
# os tipos primitivos são:
# int => para inteiros
# str => para textos, Strings
# float => para números flutuantes ou frações
# Boolean = para Verdadeiro ou Falso
print (type(1), type(2), type(-10), "Função 'type' para identificar o tipo de dados:")
print (type("Olá, tudo bem ?"), "str ou String para textos")
print(type(1.1),"float para números flutuantes")
print(type(10 == 10),"bool ou boolean para 'Verdadeiro ou Falso, True ou False \n neste exemplo estamos perguntando se 10 é igual a 10 ")
print("e a resposta foi 'Verdadeiro' ou True, logo é do tipo boolean")